#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (ac > 1)
	{
		while (av[j])
		{
			while (av[ac - 1][i])
			{
				write(1, &av[ac - 1][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j--;
			i = 0;
		}
	}
	return (0);
}
