#include<unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	int	lett;

	lett = 97;
	while (lett <= 122)
	{
		ft_putchar(lett++);
	}
}
